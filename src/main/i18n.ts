/** Translate `key` in `lang`, substituting {var} placeholders from `vars`. */
export function t(
  lang: Lang,
  key: string,
  vars?: Record<string, string | number>
): string {
  let s = STRINGS[lang][key] ?? STRINGS.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace("{" + k + "}", String(v));
    }
  }
  return s;
}
